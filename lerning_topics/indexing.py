# 1:39:20
# [start : end : step]

credit_number = "12132-2324-4546-6575"

# print(credit_number[:4])
print(credit_number[0:4])
# from  index 4 till the end
print(credit_number[4:])

# from the end (last one -1)
print(credit_number[-1])

#step  will print every second caracter
print(credit_number[::3])
# last for
print(credit_number[-4:])
# last 4 but from last to previous
print(credit_number[-1:-5:-1])

# reverse the string
print(credit_number[::-1])
